from django.http import HttpResponse
from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.pdfgen import canvas


def render_classic_pdf(cv):
    response = HttpResponse(content_type="application/pdf")
    response["Content-Disposition"] = f'attachment; filename="{cv.full_name}_Classic_CV.pdf"'

    p = canvas.Canvas(response, pagesize=A4)
    width, height = A4

    x = 60
    y = height - 60

    # -------------------------
    # HEADER (Classic style)
    # -------------------------
    p.setFont("Times-Bold", 20)
    p.drawString(x, y, cv.full_name or "No Name")

    y -= 25

    p.setFont("Times-Roman", 12)
    p.drawString(x, y, cv.job_title or "")

    y -= 10
    p.line(x, y, width - 60, y)

    y -= 30

    # -------------------------
    # SECTION HELPER
    # -------------------------
    def section(title, text):
        nonlocal y

        # Section title
        p.setFont("Times-Bold", 14)
        p.drawString(x, y, title)

        y -= 18

        p.setFont("Times-Roman", 10)

        if text:
            for line in text.split("\n"):
                clean = line.strip()
                if clean:
                    p.drawString(x, y, clean[:120])
                    y -= 14
        else:
            p.drawString(x, y, "N/A")
            y -= 14

        y -= 10

    # -------------------------
    # SECTIONS
    # -------------------------
    section("Professional Summary", cv.professional_summary)
    section("Skills", cv.skills)
    section("Experience", cv.experience)
    section("Education", cv.education)

    # -------------------------
    # FOOTER LINE
    # -------------------------
    y -= 20
    p.line(x, y, width - 60, y)

    p.setFont("Times-Italic", 9)
    p.drawString(x, y - 15, "Generated by AI CV Builder")

    p.showPage()
    p.save()

    return response


