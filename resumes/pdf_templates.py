# Helper
# ---------------------------------------------------

def draw_wrapped_text(pdf, text, x, y, width=90, line_height=14):
    """
    Draws multi-line text.
    This is a simple implementation that we'll improve later.
    """

    if not text:
        pdf.drawString(x, y, "N/A")
        return y - line_height

    for line in text.split("\n"):
        pdf.drawString(x, y, line[:width])
        y -= line_height

    return y


