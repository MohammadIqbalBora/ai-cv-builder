def render_modern_pdf(cv):

    response = HttpResponse(content_type="application/pdf")
    response["Content-Disposition"] = (
        f'attachment; filename="{cv.full_name}_Modern.pdf"'
    )

    pdf = canvas.Canvas(response, pagesize=A4)

    width, height = A4

    # -------------------------
    # Blue sidebar
    # -------------------------

    sidebar_width = 160

    pdf.setFillColor(colors.HexColor("#1E3A8A"))
    pdf.rect(0, 0, sidebar_width, height, fill=True, stroke=False)

    pdf.setFillColor(colors.white)

    y = height - 60

    pdf.setFont("Helvetica-Bold", 16)
    pdf.drawString(20, y, "CONTACT")

    y -= 30

    pdf.setFont("Helvetica", 10)
    pdf.drawString(20, y, cv.email or "")
    y -= 18

    pdf.drawString(20, y, cv.phone or "")
    y -= 18

    pdf.drawString(20, y, cv.address or "")

    y -= 45

    pdf.setFont("Helvetica-Bold", 16)
    pdf.drawString(20, y, "SKILLS")

    y -= 25

    pdf.setFont("Helvetica", 10)

    if cv.skills:
        for skill in cv.skills.split("\n"):
            pdf.drawString(20, y, "• " + skill)
            y -= 16

    # -------------------------
    # Main section
    # -------------------------

    x = 190
    y = height - 60

    pdf.setFillColor(colors.black)

    pdf.setFont("Helvetica-Bold", 24)
    pdf.drawString(x, y, cv.full_name)

    y -= 30

    pdf.setFont("Helvetica", 14)
    pdf.drawString(x, y, cv.job_title or "")

    y -= 45

    pdf.setFont("Helvetica-Bold", 16)
    pdf.drawString(x, y, "Professional Summary")

    y -= 22

    pdf.setFont("Helvetica", 10)
    y = draw_wrapped_text(pdf, cv.professional_summary, x, y)

    y -= 20

    pdf.setFont("Helvetica-Bold", 16)
    pdf.drawString(x, y, "Experience")

    y -= 22

    pdf.setFont("Helvetica", 10)
    y = draw_wrapped_text(pdf, cv.experience, x, y)

    y -= 20

    pdf.setFont("Helvetica-Bold", 16)
    pdf.drawString(x, y, "Education")

    y -= 22

    pdf.setFont("Helvetica", 10)
    draw_wrapped_text(pdf, cv.education, x, y)

    pdf.save()

    return response


