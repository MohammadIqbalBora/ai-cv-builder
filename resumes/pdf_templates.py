def render_executive_pdf(cv):
    response = HttpResponse(content_type="application/pdf")
    response["Content-Disposition"] = f'attachment; filename="{cv.full_name}_Executive_CV.pdf"'

    p = canvas.Canvas(response, pagesize=A4)
    width, height = A4

    # -------------------------
    # LAYOUT SETTINGS
    # -------------------------
    left_x = 50
    right_x = 340
    y = height - 60

    # =========================
    # LEFT COLUMN (Sidebar)
    # =========================
    p.setFont("Helvetica-Bold", 16)
    p.drawString(left_x, y, cv.full_name or "No Name")

    y -= 20

    p.setFont("Helvetica", 10)
    p.drawString(left_x, y, cv.job_title or "")

    y -= 30

    # Contact placeholder (you can expand later)
    p.setFont("Helvetica-Bold", 11)
    p.drawString(left_x, y, "PROFILE")
    y -= 15

    p.setFont("Helvetica", 9)
    for line in (cv.address or "").split("\n"):
        p.drawString(left_x, y, line[:40])
        y -= 12

    if cv.email:
        p.drawString(left_x, y, cv.email)
        y -= 12

    if cv.phone:
        p.drawString(left_x, y, cv.phone)
        y -= 20

    # Skills block
    p.setFont("Helvetica-Bold", 11)
    p.drawString(left_x, y, "SKILLS")
    y -= 15

    p.setFont("Helvetica", 9)
    for line in (cv.skills or "").split("\n"):
        p.drawString(left_x, y, f"- {line[:40]}")
        y -= 12

    # =========================
    # RIGHT COLUMN (Main CV)
    # =========================
    y_right = height - 60

    def section(title, text, y_pos):
        p.setFont("Helvetica-Bold", 12)
        p.drawString(right_x, y_pos, title)
        y_pos -= 15

        p.setFont("Helvetica", 9)

        if text:
            for line in text.split("\n"):
                clean = line.strip()
                if clean:
                    p.drawString(right_x, y_pos, clean[:80])
                    y_pos -= 12
        else:
            p.drawString(right_x, y_pos, "N/A")
            y_pos -= 12

        y_pos -= 15
        return y_pos

    y_right = section("PROFESSIONAL SUMMARY", cv.professional_summary, y_right)
    y_right = section("EXPERIENCE", cv.experience, y_right)
    y_right = section("EDUCATION", cv.education, y_right)

    # Footer line
    p.line(50, 40, width - 50, 40)

    p.setFont("Helvetica-Oblique", 8)
    p.drawString(50, 25, "Generated by AI CV Builder - Executive Template")

    p.showPage()
    p.save()

    return response